<template>
  <div class="tab-control">
    <div class="tab-control-item" v-for="(item,index) in title" :key="item" :class="{active: index === currentIndex}" @click="itemClick(index)">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: 'TabControl',
  data () {
    return {
      currentIndex:0,
    }
  },
  props: {
    title:{
      type:Array,
      default(){
        return [];
      }
    }
  },
  components: {},
  watch: {},
  computed: {},
  methods: {
    itemClick(index) {
      this.currentIndex = index;
      this.$emit('tab-control',index);
    }
  },
  created () {},
  mounted () {}
}
</script>

